// Compute the area of a circle.
class DoubleDemo {
public static void main(String[] args) {
    double pi, r, a;

    //input
    r = 10.8;// radius
    pi = 3.1416; // pi, approximately

    //computation
    a = pi * r * r; // compute area

    //output
    System.out.println("Area of circle is " + a); }
}
